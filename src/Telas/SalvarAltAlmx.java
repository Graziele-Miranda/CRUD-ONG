        AltAlmoxarifado alt = new AltAlmoxarifado();
        
        Almoxarifado g = new Almoxarifado();    
        g.setId(Integer.parseInt(idalm.getText()));
        g.setNome(nome.getText());
        
        g.setCat((String) combocat.getSelectedItem());
        g.setQuant(Integer.parseInt(qtd.getText()));
        
         
