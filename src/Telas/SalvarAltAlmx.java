    public void alterar(Integer id){
        try {
            AlmxDAO dao = new AlmxDAO();
            List<Almoxarifado> listCao = dao.ConsProdId(id);
            
            for(Almoxarifado f : listCao){
                idalm.setText(String.valueOf(f.getId()));
                nome.setText(f.getNome());
                combocat.setSelectedItem(String.valueOf(f.getCat()));
                qtd.setText(String.valueOf(f.getQuant()));
                
            }
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }
